package arreglos;

public class Enemigo {
    
    public String nombre;
    public int edad;

    public Enemigo(String nombre, int edad) {
        this.nombre = nombre;
        this.edad = edad;
    }

    public Enemigo() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
    
    
    
}
